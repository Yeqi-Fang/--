# 创建字典
person = {'name': 'John', 'age': 25, 'city': 'New York'}

# 访问字典元素
print(person['name'])  # 输出: 'John'

# 修改字典元素
person['age'] = 30
print(person)  # 输出: {'name': 'John', 'age': 30, 'city': 'New York'}

# 添加新键值对
person['occupation'] = 'Engineer'
print(person)  # 输出: {'name': 'John', 'age': 30, 'city': 'New York', 'occupation': 'Engineer'}

# 删除键值对
del person['city']
print(person)  # 输出: {'name': 'John', 'age': 30, 'occupation': 'Engineer'}
