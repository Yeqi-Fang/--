# 创建列表
fruits = ['apple', 'banana', 'cherry']

# 访问列表元素
print(fruits[0])  # 输出: 'apple'

# 修改列表元素
fruits[1] = 'pear'
print(fruits)  # 输出: ['apple', 'pear', 'cherry']

# 添加元素到列表末尾
fruits.append('orange')
print(fruits)  # 输出: ['apple', 'pear', 'cherry', 'orange']

# 删除列表元素
del fruits[0]
print(fruits)  # 输出: ['pear', 'cherry', 'orange']

