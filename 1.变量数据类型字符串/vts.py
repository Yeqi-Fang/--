# Variables
name = "John"
age = 25
is_student = True

# Data types
# String
greeting = "Hello, world!"
# Integer
quantity = 10
# Float
price = 4.99
# Boolean
is_valid = False

# Printing variable values and types
print("Name:", name, "| Type:", type(name))
print("Age:", age, "| Type:", type(age))
print("Is student:", is_student, "| Type:", type(is_student))

print("Greeting:", greeting, "| Type:", type(greeting))
print("Quantity:", quantity, "| Type:", type(quantity))
print("Price:", price, "| Type:", type(price))
print("Is valid:", is_valid, "| Type:", type(is_valid))
